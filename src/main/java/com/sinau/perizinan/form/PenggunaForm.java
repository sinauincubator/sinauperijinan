package com.sinau.perizinan.form;

public class PenggunaForm {
	private String idPengguna;
	private String namaLengkap;
	private String roleManajemen;
	private String userID;

	public String getIdPengguna() {
		return idPengguna;
	}
	public void setIdPengguna(String idPengguna) {
		this.idPengguna = idPengguna;
	}
	public String getNamaLengkap() {
		return namaLengkap;
	}
	public void setNamaLengkap(String namaLengkap) {
		this.namaLengkap = namaLengkap;
	}
	public String getRoleManajemen() {
		return roleManajemen;
	}
	public void setRoleManajemen(String roleManajemen) {
		this.roleManajemen = roleManajemen;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}





}
