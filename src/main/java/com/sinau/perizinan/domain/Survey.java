package com.sinau.perizinan.domain;

public class Survey {
	private String noPermintaan;
	private String pemohon;
	private String nama;
	private String jenisKelamin;
	private String noKTP;
	private String tglTerima;
	private String telepon;
	private String jmlIzinBelumSurvey;

	public String getNoPermintaan() {
		return noPermintaan;
	}
	public void setNoPermintaan(String noPermintaan) {
		this.noPermintaan = noPermintaan;
	}
	public String getPemohon() {
		return pemohon;
	}
	public void setPemohon(String pemohon) {
		this.pemohon = pemohon;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getJenisKelamin() {
		return jenisKelamin;
	}
	public void setJenisKelamin(String jenisKelamin) {
		this.jenisKelamin = jenisKelamin;
	}
	public String getNoKTP() {
		return noKTP;
	}
	public void setNoKTP(String noKTP) {
		this.noKTP = noKTP;
	}
	public String getTglTerima() {
		return tglTerima;
	}
	public void setTglTerima(String tglTerima) {
		this.tglTerima = tglTerima;
	}
	public String getTelepon() {
		return telepon;
	}
	public void setTelepon(String telepon) {
		this.telepon = telepon;
	}
	public String getJmlIzinBelumSurvey() {
		return jmlIzinBelumSurvey;
	}
	public void setJmlIzinBelumSurvey(String jmlIzinBelumSurvey) {
		this.jmlIzinBelumSurvey = jmlIzinBelumSurvey;
	}




}
